public class Variablendeklaration {

    private int pears;

    public static void main(String[] args) {

        int pears = 3;

        System.out.println("Pears (3): " + pears);
        pears = pears + 5;
        System.out.println("Pears (8): " + pears);
        int apples = 6;
        System.out.println("Apples (6): " + apples);
        apples = apples - 2;
        System.out.println("Apples (4): " + apples);


    }
}
